async def main():
    logger.info("–ó–∞–ø—É—Å–∫ –±–æ—Ç–∞...")

    logger.info("–ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö...")
